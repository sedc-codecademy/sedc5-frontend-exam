class Novel {
	constructor (title, author, publisher, yearOfPublication, pages, series, ISBN, review){
		this.title = title;
		this.author = author;
		this.publisher = publisher;
		this.year = yearOfPublication;
		this.pages = pages;
		this.series = series;
		this.ISBN = ISBN;
		this.review = review;
	}
};

class Anthology {
	constructor (title, editor, publisher, yearOfPublication, pages, story, ISBN, review){
		this.title = title;
		this.editor = editor;
		this.publisher = publisher;
		this.year= yearOfPublication;
		this.pages = pages;
		this.story = story;
		this.ISBN = ISBN;
		this.review = review;
	}
};
