class Book {
    constructor(id, kind, title, author, publisher, yearOfPublication, length, ISBN, review) {
        this.id = id;
        this.kind = kind;
        this.title = title;
        this.author = author;
        this.publisher = publisher;
        this.yearOfPublication = yearOfPublication;
        this.length = length;
        this.ISBN = ISBN;
        this.review = review;
    }
}